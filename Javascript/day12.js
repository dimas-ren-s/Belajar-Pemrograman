// execution context, hoisting, scope, dan closure
// Execution Context : 
// Sebuah konteks dimana code dijalankan sesuai posisinya

// Hoisting :
// Sebuah sifat dimana, sebuah variabel di naikkan untuk dideklarasi terlebih dahulu baru kemudian di inisialisasi

// Scope :
// Sebuah cakupan dimana javascript menerapkan antara block scope(let, dan const) dan function scope(var)

// closure :
// Sebuah function yang dijalankan setelah function lain selesai di eksekusi